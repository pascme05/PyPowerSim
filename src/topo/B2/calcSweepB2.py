"""
This function calculates the results for a parameter sweep of the B2 half-bridge circuit.
Inputs:     1) mdl:     all models and transfer functions of the architecture
            2) para:    all parameters used in the simulation
            3) setup:   includes all simulation variables
Outputs:    1) time:    results in the time domain
            2) freq:    results in the frequency domain
            3) dist:    results in the distortion domain
"""

#######################################################################################################################
# Import libs
#######################################################################################################################
# ==============================================================================
# Internal
# ==============================================================================
from src.topo.B2.calcSSeqB2 import calcSSeqB2_CB, calcSSeqB2_FF, calcSSeqB2_OPP
from src.topo.B2.calcDistB2 import calcDistB2_Ana
from src.general.calcDistNum import calcDistNum
from src.general.calcFreq import calcFreq
from src.topo.B2.calcTimeB2 import calcTimeB2
from src.topo.B2.initB2 import initB2
from src.pwm.genWaveform import genWave
from src.topo.B2.outB2 import outB2_Sweep

# ==============================================================================
# External
# ==============================================================================
import numpy as np
import math
from tqdm import tqdm


#######################################################################################################################
# Function
#######################################################################################################################
def calcSweepB2(mdl, _, setup):
    ###################################################################################################################
    # MSG IN
    ###################################################################################################################
    print("START: Sweeping B2 bridge")

    ###################################################################################################################
    # Initialisation
    ###################################################################################################################
    # ==============================================================================
    # Parameters
    # ==============================================================================
    fel = setup['Top']['fel']
    fsim = setup['Exp']['fsim']
    N = int(fsim/fel)
    K = setup['Dat']['stat']['cyc']
    W = setup['Dat']['stat']['W'] 
    Mi = setup['Dat']['stat']['Mi']

    # ==============================================================================
    # Variables
    # ==============================================================================
    # ------------------------------------------
    # Init 
    # ------------------------------------------
    [_, _, _, _, _, _, _, distAc, distDc] = initB2(W)

    # ------------------------------------------
    # Inputs 
    # ------------------------------------------
    E = setup['Top']['E']
    Vdc = setup['Dat']['stat']['Vdc']
    phiE = math.radians(setup['Top']['phiE'])
    phiV = math.radians(setup['Dat']['stat']['phi'])

    ###################################################################################################################
    # Pre-Processing
    ###################################################################################################################
    # ==============================================================================
    # Generate Reference Waveform
    # ==============================================================================
    # ------------------------------------------
    # Time
    # ------------------------------------------
    t = np.linspace(0, K / fel, K * N + 1)

    # ------------------------------------------
    # Reference
    # ------------------------------------------
    v_ref = (Vdc / 2) * Mi * genWave(t, fel, phiV, setup)
    e_ref = E * genWave(t, fel, phiE, setup)

    # ==============================================================================
    # Maximum Modulation Index
    # ==============================================================================
    Mi_max = 1
    if Mi > Mi_max:
        Mi_max = Mi
    M_i = np.linspace(1e-3, Mi_max-1e-3, W)
    
    # ==============================================================================
    # Start and End
    # ==============================================================================
    start = int(N) * 2
    ende = int(K * N + 1)
    
    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # Stationary
    # ==============================================================================
    # ------------------------------------------
    # Switching Function
    # ------------------------------------------
    if setup['Par']['PWM']['type'] == "FF":
        [xs, xsh, s, c] = calcSSeqB2_FF(v_ref, t, Mi, setup['Par'], setup['Top'])
    elif setup['Par']['PWM']['type'] == "CB":
        [xs, xsh, s, c] = calcSSeqB2_CB(v_ref, t, Mi, setup['Par'], setup['Top'])
    elif setup['Par']['PWM']['type'] == "OPP":
        [xs, xsh, s, c] = calcSSeqB2_OPP(v_ref, t, Mi, setup['Par'], setup['Top'])
    else:
        [xs, xsh, s, c] = calcSSeqB2_CB(v_ref, t, Mi, setup['Par'], setup['Top'])

    # ------------------------------------------
    # Time Domain
    # ------------------------------------------
    [timeAc, timeDc] = calcTimeB2(t, s, e_ref, Vdc, Mi, mdl, setup['Top'], start, ende)
    
    # ==============================================================================
    # Sweeping
    # ==============================================================================
    for i in tqdm(range(len(M_i)), desc='Sweep'):
        # ------------------------------------------
        # Switching
        # ------------------------------------------
        if setup['Par']['PWM']['type'] == "FF":
            [_, _, s_i, _] = calcSSeqB2_FF(v_ref, t, M_i[i], setup['Par'], setup['Top'])
        elif setup['Par']['PWM']['type'] == "CB":
            [_, _, s_i, _] = calcSSeqB2_CB(v_ref, t, M_i[i], setup['Par'], setup['Top'])
        elif setup['Par']['PWM']['type'] == "OPP":
            [_, _, s_i, _] = calcSSeqB2_OPP(v_ref, t, M_i[i], setup['Par'], setup['Top'])
        else:
            [_, _, s_i, _] = calcSSeqB2_CB(v_ref, t, M_i[i], setup['Par'], setup['Top'])
        
        # ------------------------------------------
        # Time
        # ------------------------------------------
        [tempTimeAc, tempTimeDc] = calcTimeB2(t, s_i, e_ref, Vdc, M_i[i], mdl, setup['Top'], start, ende)
        
        # ------------------------------------------
        # Distortion
        # ------------------------------------------
        [numDistAc, numDistDc] = calcDistNum(t[start:ende], tempTimeAc['i_a'], tempTimeAc['v_a'], tempTimeDc['i_dc'], tempTimeDc['v_dc'], Vdc, fel)
        [anaTimeAc, anaTimeDc] = calcDistB2_Ana(M_i[i], Vdc, setup['Top'], setup['Par'])
        
        # ------------------------------------------
        # Output
        # ------------------------------------------
        for c1 in numDistAc:
            distAc['num'][c1][i] = numDistAc[c1]
            distAc['ana'][c1][i] = anaTimeAc[c1]
        for c1 in numDistDc:
            distDc['num'][c1][i] = numDistDc[c1]
            distDc['ana'][c1][i] = anaTimeDc[c1]

    ###################################################################################################################
    # Post-Processing
    ###################################################################################################################
    # ==============================================================================
    # Frequency domain
    # ==============================================================================
    [freqSw, freqAc, freqDc] = calcFreq(s[start:ende], xs[start:ende], timeAc, timeDc)

    # ==============================================================================
    # Output
    # ==============================================================================
    [time, freq, sweep] = outB2_Sweep(distAc, distDc, timeAc, timeDc, freqSw, freqAc, freqDc, t, v_ref, e_ref, s, c, xs,
                                      xsh, W, M_i, ende, start)
    
    ###################################################################################################################
    # MSG Out
    ###################################################################################################################
    print("END: Sweeping B2 bridge")

    ###################################################################################################################
    # Return
    ###################################################################################################################
    return [time, freq, sweep]
