"""
This function calculates the results for a parameter sweep of the B4 full-bridge circuit.
Inputs:     1) mdl:     all models and transfer functions of the architecture
            2) para:    all parameters used in the simulation
            3) setup:   includes all simulation variables
Outputs:    1) time:    results in the time domain
            2) freq:    results in the frequency domain
            3) dist:    results in the distortion domain
"""

#######################################################################################################################
# Import libs
#######################################################################################################################
# ==============================================================================
# Internal
# ==============================================================================
from src.topo.B4.calcSSeqB4 import calcSSeqB4_CB, calcSSeqB4_FF, calcSSeqB4_OPP
from src.topo.B4.calcDistB4 import calcDistB4_Ana
from src.general.calcDistNum import calcDistNum
from src.topo.B4.calcTimeB4 import calcTimeB4
from src.general.calcFreq import calcFreq
from src.topo.B4.initB4 import initB4
from src.pwm.genWaveform import genWave
from src.topo.B4.outB4 import outB4_Sweep

# ==============================================================================
# External
# ==============================================================================
import numpy as np
import math
from tqdm import tqdm


#######################################################################################################################
# Function
#######################################################################################################################
def calcSweepB4(mdl, _, setup):
    ###################################################################################################################
    # MSG IN
    ###################################################################################################################
    print("START: Sweeping B4 bridge")

    ###################################################################################################################
    # Initialisation
    ###################################################################################################################
    # ==============================================================================
    # Init
    # ==============================================================================
    v_ref = {}

    # ==============================================================================
    # Parameters
    # ==============================================================================
    fel = setup['Top']['fel']
    fsim = setup['Exp']['fsim']
    N = int(fsim / fel)
    K = int(setup['Dat']['stat']['cyc'])
    W = int(setup['Dat']['stat']['W'])
    Mi = setup['Dat']['stat']['Mi']

    # ==============================================================================
    # Variables
    # ==============================================================================
    # ------------------------------------------
    # Init 
    # ------------------------------------------
    [_, _, _, _, _, _, _, distAc, distDc] = initB4(W)

    # ------------------------------------------
    # Inputs 
    # ------------------------------------------
    E = setup['Top']['E']
    Vdc = setup['Dat']['stat']['Vdc']
    phiE = math.radians(setup['Top']['phiE'])
    phiV = math.radians(setup['Dat']['stat']['phi'])

    ###################################################################################################################
    # Pre-Processing
    ###################################################################################################################
    # ==============================================================================
    # Generate Reference Waveform
    # ==============================================================================
    # ------------------------------------------
    # Time
    # ------------------------------------------
    t = np.linspace(0, K / fel, K * N + 1)

    # ------------------------------------------
    # Reference
    # ------------------------------------------
    v_ref['A'] = +(Vdc / 2) * Mi * genWave(t, fel, phiV, setup)
    v_ref['B'] = -(Vdc / 2) * Mi * genWave(t, fel, phiV, setup)
    e_ref = E * genWave(t, fel, phiE, setup)

    # ==============================================================================
    # Maximum Modulation Index
    # ==============================================================================
    Mi_max = 1
    if Mi > Mi_max:
        Mi_max = Mi
    M_i = np.linspace(1e-3, Mi_max - 1e-3, W)

    # ==============================================================================
    # Start and End
    # ==============================================================================
    start = int(N) * 2
    ende = int(K * N + 1)

    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # Stationary
    # ==============================================================================
    # ------------------------------------------
    # Switching Function
    # ------------------------------------------
    if setup['Par']['PWM']['type'] == "FF":
        [xs, xsh, s, c] = calcSSeqB4_FF(v_ref, t, Mi, setup)
    elif setup['Par']['PWM']['type'] == "CB":
        [xs, xsh, s, c] = calcSSeqB4_CB(v_ref, t, Mi, setup)
    elif setup['Par']['PWM']['type'] == "OPP":
        [xs, xsh, s, c] = calcSSeqB4_OPP(v_ref, t, Mi, setup)
    else:
        [xs, xsh, s, c] = calcSSeqB4_CB(v_ref, t, Mi, setup)

    # ------------------------------------------
    # Time Domain
    # ------------------------------------------
    [timeAc, timeDc] = calcTimeB4(t, s, e_ref, Vdc, Mi, mdl, setup, start, ende)

    # ==============================================================================
    # Sweeping
    # ==============================================================================
    for i in tqdm(range(len(M_i)), desc='Sweep'):
        # ------------------------------------------
        # Switching
        # ------------------------------------------
        if setup['Par']['PWM']['type'] == "FF":
            [_, _, s_i, _] = calcSSeqB4_FF(v_ref, t, M_i[i], setup)
        elif setup['Par']['PWM']['type'] == "CB":
            [_, _, s_i, _] = calcSSeqB4_CB(v_ref, t, M_i[i], setup)
        elif setup['Par']['PWM']['type'] == "OPP":
            [_, _, s_i, _] = calcSSeqB4_OPP(v_ref, t, M_i[i], setup)
        else:
            [_, _, s_i, _] = calcSSeqB4_CB(v_ref, t, M_i[i], setup)

        # ------------------------------------------
        # Time
        # ------------------------------------------
        [tempTimeAc, tempTimeDc] = calcTimeB4(t, s_i, e_ref, Vdc, M_i[i], mdl, setup, start, ende)

        # ------------------------------------------
        # Distortion
        # ------------------------------------------
        [numDistAc, numDistDc] = calcDistNum(t[start:ende], tempTimeAc['i_a'], tempTimeAc['v_a'],
                                             tempTimeDc['i_dc'], tempTimeDc['v_dc'], 2*Vdc, fel)
        [anaTimeAc, anaTimeDc] = calcDistB4_Ana(M_i[i], Vdc, setup)

        # ------------------------------------------
        # Output
        # ------------------------------------------
        for c1 in numDistAc:
            distAc['num'][c1][i] = numDistAc[c1]
            distAc['ana'][c1][i] = anaTimeAc[c1]
        for c1 in numDistDc:
            distDc['num'][c1][i] = numDistDc[c1]
            distDc['ana'][c1][i] = anaTimeDc[c1]

    ###################################################################################################################
    # Post-Processing
    ###################################################################################################################
    # ==============================================================================
    # Frequency domain
    # ==============================================================================
    [freqSw, freqAc, freqDc] = calcFreq(s['A'][start:ende], xs['A'][start:ende], timeAc, timeDc)

    # ==============================================================================
    # Output
    # ==============================================================================
    [time, freq, sweep] = outB4_Sweep(distAc, distDc, timeAc, timeDc, freqSw, freqAc, freqDc, t, v_ref, e_ref, s, c, xs,
                                      xsh, W, M_i, ende, start)

    ###################################################################################################################
    # MSG Out
    ###################################################################################################################
    print("END: Sweeping B4 bridge")

    ###################################################################################################################
    # Return
    ###################################################################################################################
    return [time, freq, sweep]
